class PuertoSerie  {

public:
  // .........................................................
  // .........................................................
  // N --> construcotr() -->
  PuertoSerie (long baudios) {
	Serial.begin( baudios );
	// mejor no poner esto aquí: while ( !Serial ) delay(10);   
  } // ()

  // .........................................................
  // .........................................................
  // espera 10 ms a que esté disponible
  // esperarDisponible() -->
  void esperarDisponible() {

	while ( !Serial ) {
	  delay(10);   
	}

  } // ()

  // .........................................................
  // .........................................................
  template<typename T>
  // escribe el mensaje que le pasas
  // T --> escribir() -->
  void escribir (T mensaje) {
	Serial.print( mensaje );
  } // ()
  
}; // class PuertoSerie

